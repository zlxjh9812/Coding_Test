import java.util.Scanner;

class Solution {
    public static void main(String[] args) {
         Scanner sc = new Scanner(System.in);
	        int a = sc.nextInt();
	        int b = sc.nextInt();
	        for(int i = 0;i<b;i++){
	        	for(int z = 0;z<a;z++){
	        		System.out.print("*");
	        	}
	        	System.out.println();
	        }

	      
    }
}